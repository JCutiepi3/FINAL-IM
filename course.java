    private void updateCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateCourseActionPerformed
        // TODO add your handling code here:
        try {
            stmt = conn.createStatement();
            
            // get text in the textfield and store in local variable
            // string
            String course_courseCode = course_code.getText();
            String course_courseName = course_name.getText();
            String course_courseUnit = unit.getText();
            int newCourseUnit = Integer.parseInt(course_courseUnit);
            
            
            // int
                   
                     
            // string for INSERT slq query, var name sa db then var name sa code/program
            String sql = "UPDATE course_info SET "
            + "course_name = '" + course_courseName + "', "
            + "unit = " + newCourseUnit + " " // No single quotes around newCourseUnit
            + "WHERE course_code = '" + course_courseCode + "'"; // Use single quotes if course_code is a string, otherwise remove them if it is an integer

            
            // execute query
            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Student data is successfully added!");
            
            setVisible(false);
            course object = new course();
            object.setVisible(true);
            
           
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_updateCourseActionPerformed

