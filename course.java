    private void searchMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchMeActionPerformed
        // TODO add your handling code here:
                                                    
        // TODO add your hßandling code here:

        try {
            stmt = conn.createStatement();

            String course_courseCode = course_code.getText(); 
            //int course_courseCode = Integer.parseInt(course_code.getText());
            String sql = "SELECT * FROM course_info WHERE course_code = '"+course_courseCode+"' ";

            // store the fetched student record to rs variable
            rs = stmt.executeQuery(sql);
            course_code.removeAll();

            if (rs.next()) {
                // do this for all the attributes
                // string
                course_code.setText(rs.getString("course_code"));
                course_name.setText(rs.getString("course_name"));
                unit.setText(rs.getString("unit"));

            } else {
                JOptionPane.showMessageDialog(null, "Record not found");
            }

        } catch (HeadlessException | NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);

        }                                      

    }//GEN-LAST:event_searchMeActionPerformed

